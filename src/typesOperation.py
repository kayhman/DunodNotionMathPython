# les opérations arithmétiques de base sont assurées sur les entiers et les réels
a = 1
b = 2
print(a + b)
#> 3
print(a - b)
#> -1
print(a * b)
#> 2
print(a / b)
#> 0.5

# il est possible de sommer entier et flottant
c = 3.5
print(a - b + c)
#> 2.5

